package org.aoju.bus.logger.level;

/**
 * 日志等级
 *
 * @author aoju.org
 * @version 3.0.1
 * @group 839128
 * @since JDK 1.8
 */
public enum Level {
    /**
     * 'ALL' log level.
     */
    ALL,
    /**
     * 'TRACE' log level.
     */
    TRACE,
    /**
     * 'DEBUG' log level.
     */
    DEBUG,
    /**
     * 'INFO' log level.
     */
    INFO,
    /**
     * 'WARN' log level.
     */
    WARN,
    /**
     * 'ERROR' log level.
     */
    ERROR,
    /**
     * 'FATAL' log level.
     */
    FATAL,
    /**
     * 'OFF' log.
     */
    OFF
}
